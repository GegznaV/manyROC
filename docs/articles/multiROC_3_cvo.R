## ------------------------------------------------------------------------

set.seed(1)
cvo_create_folds(block_by = DataSet1$ID, k = nFolds)

## ------------------------------------------------------------------------
Folds1_a <- cvo_create_folds(data = DataSet1,
                             stratify_by = "gr",
                             block_by = "ID",
                             nFolds,
                             returnTrain = FALSE)
# str(Folds1_a)
cvo_test_bs(Folds1_a,
            stratify_by = "gr",
            block_by = "ID",
            data = DataSet1)


